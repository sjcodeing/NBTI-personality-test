// 计算匹配度
function calculateCharacter(scores) {
  // 确定每个维度的倾向
  const M = scores.M || 0;
  const D = scores.D || 0;
  const P = scores.P || 0;
  const R = scores.R || 0;
  const J = scores.J || 0;
  const T = scores.T || 0;
  const F = scores.F || 0;
  const L = scores.L || 0;

  // 构建类型代码
  const energy = M >= D ? 'M' : 'D';
  const style = P >= R ? 'P' : 'R';
  const attitude = J >= T ? 'J' : 'T';
  const express = F >= L ? 'F' : 'L';
  const typeCode = energy + style + attitude + express;

  // 查找匹配的角色
  const character = CHARACTERS.find(c => c.id === typeCode);
  
  if (character) {
    return character;
  }

  // 如果没有精确匹配，找最接近的
  let bestMatch = CHARACTERS[0];
  let bestScore = -Infinity;

  for (const char of CHARACTERS) {
    let matchScore = 0;
    const prefs = char.dimensionPrefs || {};
    
    // 计算每个维度的匹配度
    if (energy === 'M' && prefs.M > 0) matchScore += prefs.M;
    if (energy === 'D' && prefs.D > 0) matchScore += prefs.D;
    if (style === 'P' && prefs.P > 0) matchScore += prefs.P;
    if (style === 'R' && prefs.R > 0) matchScore += prefs.R;
    if (attitude === 'J' && prefs.J > 0) matchScore += prefs.J;
    if (attitude === 'T' && prefs.T > 0) matchScore += prefs.T;
    if (express === 'F' && prefs.F > 0) matchScore += prefs.F;
    if (express === 'L' && prefs.L > 0) matchScore += prefs.L;

    if (matchScore > bestScore) {
      bestScore = matchScore;
      bestMatch = char;
    }
  }

  return bestMatch;
}

module.exports = { CHARACTERS, calculateCharacter };
